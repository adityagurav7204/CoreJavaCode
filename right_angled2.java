package star.cource;

public class right_angled2 {

	public static void main(String[] args) {
		for(int i=1; i<=5; i++)
		{
			System.out.println(" ");
			for(int j=1; j<=i; j++)
			{
				System.out.print("*");
			}
		}
		for(int i=1; i<=5; i++)
		{
			System.out.println(" ");
			for(int j=i; j<=4; j++)
			{
				System.out.print("*");
			}
		}

	}

}
